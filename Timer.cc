
#include "Timer.h"

//------------------------------------------------------------------------------
// Name: Timer
//------------------------------------------------------------------------------
Timer::Timer() {

}

//------------------------------------------------------------------------------
// Name: tick
//------------------------------------------------------------------------------
bool Timer::tick() {
	if(--timer_ == 0) {
		timer_ = frequency_;
		return true;
	}
	
	return false;
}

//------------------------------------------------------------------------------
// Name: set_frequency
//------------------------------------------------------------------------------
void Timer::set_frequency(uint16_t frequency) {
	frequency_ = frequency;
	timer_     = frequency_;
}

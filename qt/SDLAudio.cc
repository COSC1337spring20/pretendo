#include <iostream>

//------------------------------------------------------------------------------
// Name: SDLAudio
//------------------------------------------------------------------------------
SDLAudio::SDLAudio() {

	SDL_Init(SDL_INIT_AUDIO);
	SDL_AudioSpec spec;
	spec.callback = 0;
	spec.userdata = this;
	spec.channels = 1;
	spec.format   = AUDIO_U8;
	spec.freq     = 44100;
	spec.samples  = 735;
	
	SDL_OpenAudio(&spec, 0);
}

//------------------------------------------------------------------------------
// Name: ~SDLAudio
//-------------------------------------------------------------------
SDLAudio::~SDLAudio() {
	SDL_CloseAudio();
}



#include "Controller.h"

//------------------------------------------------------------------------------
// Name: Controller
//------------------------------------------------------------------------------
Controller::Controller() : data_(0), enabled_(true) {
}

//------------------------------------------------------------------------------
// Name: read
//------------------------------------------------------------------------------
uint8_t Controller::read() {
	return data_.read();
}

//------------------------------------------------------------------------------
// Name: poll
//------------------------------------------------------------------------------
void Controller::poll() {

	if(enabled_) {
        data_.load(static_cast<uint8_t>(keystate_.to_ulong()));
	}
}

